package model;

public class NhanVat {
	private String ten, namSinh, namMat, giaDinh, queQuan, suNghiep, khac, trieuDai;

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}

	public String getNamSinh() {
		return namSinh;
	}

	public void setNamSinh(String namSinh) {
		this.namSinh = namSinh;
	}

	public String getNamMat() {
		return namMat;
	}

	public void setNamMat(String namMat) {
		this.namMat = namMat;
	}

	public String getGiaDinh() {
		return giaDinh;
	}

	public void setGiaDinh(String giaDinh) {
		this.giaDinh = giaDinh;
	}

	public String getQueQuan() {
		return queQuan;
	}

	public void setQueQuan(String queQuan) {
		this.queQuan = queQuan;
	}

	public String getSuNghiep() {
		return suNghiep;
	}

	public void setSuNghiep(String suNghiep) {
		this.suNghiep = suNghiep;
	}

	public String getKhac() {
		return khac;
	}

	public void setKhac(String khac) {
		this.khac = khac;
	}

	public String getTrieuDai() {
		return trieuDai;
	}

	public void setTrieuDai(String trieuDai) {
		this.trieuDai = trieuDai;
	}
	


}
